package com.example.demo.Model;

public class model_loading_user {
    public String mLoadingUserName;
    public String mloadingUserEmail;
    public String mLoadingUserPhoneNumber;
    public String mLoadingUserPassword;
    public String mLoadingUserAvatar;

    public model_loading_user(String mLoadingUserName, String mloadingUserEmail, String mLoadingUserPhoneNumber, String mLoadingUserPassword, String mLoadingUserAvatar) {
        this.mLoadingUserName = mLoadingUserName;
        this.mloadingUserEmail = mloadingUserEmail;
        this.mLoadingUserPhoneNumber = mLoadingUserPhoneNumber;
        this.mLoadingUserPassword = mLoadingUserPassword;
        this.mLoadingUserAvatar = mLoadingUserAvatar;
    }

    public String getmLoadingUserName() {
        return mLoadingUserName;
    }

    public void setmLoadingUserName(String mLoadingUserName) {
        this.mLoadingUserName = mLoadingUserName;
    }

    public String getMloadingUserEmail() {
        return mloadingUserEmail;
    }

    public void setMloadingUserEmail(String mloadingUserEmail) {
        this.mloadingUserEmail = mloadingUserEmail;
    }

    public String getmLoadingUserPhoneNumber() {
        return mLoadingUserPhoneNumber;
    }

    public void setmLoadingUserPhoneNumber(String mLoadingUserPhoneNumber) {
        this.mLoadingUserPhoneNumber = mLoadingUserPhoneNumber;
    }

    public String getmLoadingUserPassword() {
        return mLoadingUserPassword;
    }

    public void setmLoadingUserPassword(String mLoadingUserPassword) {
        this.mLoadingUserPassword = mLoadingUserPassword;
    }

    public String getmLoadingUserAvatar() {
        return mLoadingUserAvatar;
    }

    public void setmLoadingUserAvatar(String mLoadingUserAvatar) {
        this.mLoadingUserAvatar = mLoadingUserAvatar;
    }
}
